# creare una variabile stringa contenente "  programmo in python "
# stampare le varianti della stringa in:
# upper()
# lower()
# strip()
# replace() - sostituendo "in" con "con il linguaggio"

s = "  programmo in python "
print(s.upper())
print(s.lower())
print(s.strip())
print(s.replace("in", "con il linguaggio"))

